# SH1106 Driver (Minimal implementation for displaying a framebuf)
class SH1106:
    def __init__(self, i2c, width=128, height=64, addr=0x3c):
        self.i2c = i2c
        self.addr = addr
        self.width = width
        self.height = height
        self.pages = self.height // 8
        self.buffer = bytearray(self.width * self.pages)
        self.init_display()

    def write_cmd(self, cmd):
        self.i2c.writeto(self.addr, b'\x00' + bytes([cmd]))

    def init_display(self):
        # Standard SH1106 initialization sequence
        for cmd in [
            0xAE,       # Display OFF
            0xD5, 0x80, # Set Display Clock Divide Ratio/Oscillator Frequency
            0xA8, 0x3F, # Set Multiplex Ratio (64 lines)
            0xD3, 0x00, # Set Display Offset
            0x40,       # Set Display Start Line
            0xAD, 0x8B, # DC-DC Control Mode (External VCC)
            0xA1,       # Set Segment Re-map (A0/A1)
            0xC8,       # Set COM Output Scan Direction (C0/C8)
            0xDA, 0x12, # Set COM Pins Hardware Configuration
            0x81, 0xBF, # Set Contrast Control
            0xD9, 0x22, # Set Pre-charge Period
            0xDB, 0x40, # Set VCOMH Deselect Level
            0xA4,       # Entire Display ON (Resume)
            0xA6,       # Set Normal Display
            0xAF        # Display ON
        ]:
            self.write_cmd(cmd)

    def show(self):
        for page in range(self.pages):
            self.write_cmd(0xB0 + page) # Set Page Address
            self.write_cmd(0x02)        # Set Lower Column Address (SH1106 offset)
            self.write_cmd(0x10)        # Set Higher Column Address
            self.i2c.writeto(self.addr, b'\x40' + self.buffer[page * self.width : (page + 1) * self.width])

